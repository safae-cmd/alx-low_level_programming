#include "3-calc.h"

/**
 * op_add - calc sum
 * @a: first number
 * @b: second number
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - calc substraction
 * @a: first number
 * @b: second number
 * Return: sum
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - calc mul
 * @a: first number
 * @b: second number
 * Return: sum
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - calc division
 * @a: first number
 * @b: second number
 * Return: sum
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calc modulo
 * @a: first number
 * @b: second number
 * Return: sum
 */
int op_mod(int a, int b)
{
	return (a % b);
}
